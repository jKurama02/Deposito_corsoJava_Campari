
public class ProvaVariabili
{
	public static void main(String[] args)
	{
		// test random
		int provaNumero = 12;
		int due = 2;
		String provaTesto = " Ciao Mondo ";
		boolean provaBool = true;
		System.out.println(provaNumero + due + provaTesto + provaBool);

		// print of variable
		int num_x1 = 5;
		int num_y1 = 6;
		System.out.println(num_x1 + num_y1);

		int num_x2 = 5, num_y2 = 6, num_z2 = 50;
		System.out.println(num_x2 + num_y2 + num_z2);

		// primitive type
		float myFloat = 4.6f;
		double myDouble = 13.3d;
		System.out.println(myFloat);
		System.out.println(myDouble);


		// true & false

		boolean isJava = true;
		boolean isFerrari = false;

		System.out.println("isJava = " +  isJava);
		System.out.println("isFerrari = " +  isFerrari);


		// char single
		char a = 65;
		System.out.println(a);



	}
}
